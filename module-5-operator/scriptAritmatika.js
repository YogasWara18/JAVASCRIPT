/* OPERATOR ARITMATIKA */

// + ( Penjumlahan )
let x = 5 
let y = 10 
let sum = x + y         // Operator penjumlahan ( + ) untuk kita bisa menambahkan sesuatu 

console.log(sum, typeof sum )  // Output: 15 number, Tipe data mengikuti dari value ( nilai ) dari variabel.

// - ( Pengurangan )
let a = 45
let b = 32
let diff = a - b        // Operator pengurangan ( - ) dilakukan untuk mengurangi sesuatu 

console.log(diff, typeof diff)  // Output: 13 number, Tipe data mengikuti dari value ( nilai ) dari variabel.

// * ( Perkalian )
let p = 5
let q = 8
let result = p * q      // Operator perkalian ( * ) dilakukan untuk mengkali sesuatu 

console.log(result, typeof result)  // Output: 40 number, Tipe data mengikuti dari value ( nilai ) dari variabel.

// / ( Pembagian )
let g = 100
let s = 5 
let bagi = g / s        // Operator pembagian ( / ) dilakukan untuk membagi sesuatu

console.log(bagi, typeof bagi)  // Output : 20 number, Tipe data mengikuti dari value ( nilai ) dari variabel.

// % ( Modulus: Sisa bagi )
let t = 23
let r = 3 
let modulus = t % r         // Operator Modulus:sisa bagi ( % ) dilakukan menghitung sisa bagi dengan menghitung nilai terdekat dari nilai yang dibagi.

console.log(modulus, typeof modulus) // Output: 2 number, Tipe data mengikuti dari value ( nilai ) dari variabel.

// ** ( Eksponen )
let base = 2
let exponent = 6
let power = base ** exponent        // Operator Eksponen untuk bilangan nilai yang dipangkatkan, Jumlah hasil dari operator eksponen ini adalah jumlah yang sudah di hitung pangkatnya 

console.log(power, typeof power)   // Output: 64 number, Tipe data mengikuti value ( nilai ) dari variabel.

// ++ ( Increment = Ditambah 1 )
let count = 9
count++             // Operator Increment = ditambah satu ( 1 ), Setiap nilai dari variabel akan ditambah dengan 1 jika memakai operator Increment.

console.log(count, typeof count)  // Output: 10 number

// -- ( Decrement = Dikurang 1 )
let decremen = 99
decremen--          // Operator Decrement = Dikurang satu ( 1 ), setiap nilai dari variabel akan dikurang dengan 1 jika memekai operator Decrement.

console.log(decremen, typeof decremen) // Output: 98 number